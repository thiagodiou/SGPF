package br.com.sgpf.vo;

public class FuncaoEDadoVO {
	
	private String nome;
	private String elementoContagem;
	private Long quantidadeTR;
	private Long quantidadeTD;
	private String complexidade;
	private Long valorPF;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getElementoContagem() {
		return elementoContagem;
	}
	public void setElementoContagem(String elementoContagem) {
		this.elementoContagem = elementoContagem;
	}
	public Long getQuantidadeTR() {
		return quantidadeTR;
	}
	public void setQuantidadeTR(Long quantidadeTR) {
		this.quantidadeTR = quantidadeTR;
	}
	public Long getQuantidadeTD() {
		return quantidadeTD;
	}
	public void setQuantidadeTD(Long quantidadeTD) {
		this.quantidadeTD = quantidadeTD;
	}
	public String getComplexidade() {
		return complexidade;
	}
	public void setComplexidade(String complexidade) {
		this.complexidade = complexidade;
	}
	public Long getValorPF() {
		return valorPF;
	}
	public void setValorPF(Long valorPF) {
		this.valorPF = valorPF;
	}
	
	
	
}
